    @Override
    public void newNotification(String topicName) {
        if (topicName.equals(topicName)) {
            //JOptionPane.showMessageDialog(null, "Cineva a ciupit");
            jTextArea1.setText(jTextArea1.getText() + "*** ciupealaaaa ***\n");
        }
    }
    
    @Override
    public void newDataNotification(Object obj, String topicName) {        
        if (topicName.equals(topicName) && obj instanceof String) {
            final String msg = (String) obj;
            
            if (msg.equals(lastSent)) {
                return;
            }
            
            if (msg.contains(NotificationType.TOPIC_DELETED.name())) {
                formWindowClosing(null);
                JOptionPane.showMessageDialog(null, "The topic you are listening to has been deleted. Frame closing..");
                dispose();
                return;
            }
            
            jTextArea1.setText(jTextArea1.getText() + msg + "\n");            
        }
    }
    

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String toBeSent = ClientNotificationController.getInstance().getClientId() +
                "$ " +
                jTextArea2.getText();
        
        newDataNotification(toBeSent, topicName);
        lastSent = toBeSent;
        
        long start = System.currentTimeMillis();
        ClientNotificationAsyncController.getInstance().postDataNotification(toBeSent, topicName);
        long end = System.currentTimeMillis();
        
        jLabel4.setText(String.valueOf(end - start));
        jLabel6.setText("ClientNotificationController#postDataNotification");
        jTextArea2.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

